<h1>How Story Walk Works</h1>

<h3>1. Find the <em>Start</em> code.</h3>

<img src="scanstart.png" alt="Scan the Start code" style="width:50%;height:auto;display:block;margin-left:auto;margin-right:auto;">

<p>This code will be clearly labeled <em>Start</em>.
Scan it using a QR Reader application of your choice.</p>

<h3>2. Select a story.</h3>

<img src="stories.png" alt="Select a story" style="width:50%;height:auto;display:block;margin-left:auto;margin-right:auto;">

<p>After scanning the <em>Start</em> code, you will be
shown our full list of available stories. Tap on the one
you'd like to read. When you see the book cover page,
you'll know you're set!</p>

<h3>3. Start scanning.</h3>

<img src="theend.png" alt="Select a story" style="width:50%;height:auto;display:block;margin-left:auto;margin-right:auto;">

<p>Now that you've chosen your story, start looking for
the other numbered codes to read the rest of the story.
You'll know you're through with yours when you hit
<strong>The End.</strong></p>

<h3>4. Read another story!</h3>

<p>We've got multiple stories, and all our codes work the
same way with all of them. Head back to the <em>Start</em>
code, scan it, and choose a new story to read!</p>

