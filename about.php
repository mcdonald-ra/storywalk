<h1>About</h1>

<p>Story Walk is a creative endeavor that seeks to promote reading,
fitness, and computer literacy in young students.</p>

<p>Story Walk contains a multitude of public domain stories suitable
for children, and is constantly working to expand the offerings available.</p>

<p>This web application is something of a scavenger hunt combined with story time.
We have a selection of numbered <a href="http://simple.wikipedia.org/wiki/SQR_codes">QR Codes</a>
that can be distributed around an area, such as we've distributed them about Tattnall Square Park
in Macon, Georgia. Readers can use a QR Reader (available on any smartphone and most tablets)
to scan each code to read a story page by page.</p>

<p>For more information on how Story Walk works, click <a href="aboutStory.php">here.</a> If
you'd like more information on the team behind Story Walk, click <a href="aboutStaff.php">here.
</a></p>

