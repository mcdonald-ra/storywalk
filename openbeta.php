<h1>Story Walk is Now in Open Beta!</h1>

<p>After two months' worth of intensive design and programming,
Story Walk is now being opened up for a public beta!</p>

<p>This means that the team behind Story Walk has completed enough of
the groundwork of the site to allow users to publicly access and use
the site as it stands. Unfortunately, because it <em>is</em> a beta release,
there are many bugs and glitches in the site that still need to be ironed out.
Rest assured, the Story Walk team is aware of many of these issues, and is
hard at work fixing them.</p>

<p>One of the most prevalent issues is the fact that, as it is merely a beta
release, we are unable to offer Story Walk outside of the city in which it
was developed, as it requires physical accessories that we are as of yet unable
to provide for places outside of our city. For the time being, the only Story Walk
course available is in Tattnall Square Park, in Macon, Georgia. As the site grows
further, we may work on spreading Story Walk courses throughout the city, state,
country, or even globe, but for the time being, it is restricted to those that
Story Walk personnel set up.</p>

